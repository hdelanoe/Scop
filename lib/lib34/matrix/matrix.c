#include "../lib34.h"

t_mat4	mat4(t_vec3 v)
{
	t_mat4 r;

	r.m[0][0] = v.x;
	r.m[0][1] = 0.0f;
	r.m[0][2] = 0.0f;
	r.m[0][3] = 0.0f;
	r.m[1][0] = 0.0f;
	r.m[1][1] = v.y;
	r.m[1][2] = 0.0f;
	r.m[1][3] = 0.0f;
	r.m[2][0] = 0.0f;
	r.m[2][1] = 0.0f;
	r.m[2][2] = v.z;
	r.m[2][3] = 0.0f;
	r.m[3][0] = 0.0f;
	r.m[3][1] = 0.0f;
	r.m[3][2] = 0.0f;
	r.m[3][3] = 1.0f;
	return (r);
}

t_mat4	translate(t_mat4 m, t_vec3 v)
{
	m.m[0][3] = v.x;
	m.m[1][3] = v.y;
	m.m[2][3] = v.z;
	return (m);
}

t_mat4	scale(t_vec3 v)
{
	t_mat4 m;

	m = mat4(v);
	return (m);
}
