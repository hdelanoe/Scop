#include "../lib34.h"

t_mat4		mat4_mult(t_mat4 a, t_mat4 b)
{
	t_mat4		r;
	int			i;
	int			j;

	i = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
		{
			r.m[i][j] =
			a.m[i][0] * b.m[0][j] +
			a.m[i][1] * b.m[1][j] +
			a.m[i][2] * b.m[2][j] +
			a.m[i][3] * b.m[3][j];
		}
	}
	return (r);
}
